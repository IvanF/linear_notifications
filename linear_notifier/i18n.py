def _fallback_lang() -> str:
    return "en" if _CURRENT != "en" else "ru"


def translate_notification_type(ntype: str) -> str:
    """Человекочитаемый тип уведомления на текущем языке."""
    if not ntype:
        return tr("generic_notification")
    table = _NOTIFICATION_TYPES.get(_CURRENT) or _NOTIFICATION_TYPES["en"]
    if ntype in table:
        return table[ntype]
    spaced = re.sub(r"([a-z])([A-Z])", r"\1 \2", ntype)
    spaced = spaced.replace("_", " ").strip()
    return spaced or ntype


def load_language_from_config() -> None:
    global _CURRENT
    cfg = load_config()
    lang = cfg.get("language", "ru")
    if lang in LANG_CODES:
        _CURRENT = lang
    else:
        _CURRENT = "ru"


def get_language() -> str:
    return _CURRENT


def set_language(code: str) -> None:
    global _CURRENT
    if code in LANG_CODES:
        _CURRENT = code


