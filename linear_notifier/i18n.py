def tr(key: str, **kwargs: Any) -> str:
    lang = _CURRENT
    d = _MSG.get(lang) or _MSG["en"]
    s = d.get(key)
    if s is None:
        s = _MSG["en"].get(key) or key
    if kwargs:
        try:
            return s.format(**kwargs)
        except Exception:
            return s
    return s


def restart_application() -> None:
    """Перезапуск процесса с тем же argv (после смены языка)."""
    sys.stdout.flush()
    sys.stderr.flush()
    os.execv(sys.argv[0], sys.argv)
